La formación de mamas en hombres de igual forma puede ser un efecto secundario de un problema renal o de tiroides, también los tumores de la cápsula suprarrenal o de testículo pueden formar la base de los pechos hinchados en ese caso, la cirugía cosmética no es recomendable, lo que necesita es un tratamiento médico. Este problema se presenta cada vez más en hombres mayores de 35 años. Esta forma de aumento de mamas no desaparece espontáneamente. Una intervención cosmética puede ofrecer la solución.
En general, la ginecomastia no es un problema grave, pero puede causar malestar o inconformidad importante en los pacientes que la presentan. Hombres y niños con ginecomastia a veces tienen dolor en el pecho y muchas veces presentan falta de confianza en su imagen o pueden sentirse avergonzados.
El objetivo del tratamiento en pacientes con ginecomastia es reducir el tamaño de los senos que han aumentado de volumen, restaurar un contorno de pecho masculino normal y corregir las deformidades de la mama, el pezón o la areola. Los métodos de reducción comunes incluyen la liposucción, la extracción del exceso de tejido glandular o la utilización de una combinación de liposucción y escisión.
