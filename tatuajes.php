La luz del láser actúa fraccionando las partículas de tinta o pigmento y las altera químicamente. De esta forma, las partículas son reabsorbidas por el organismo a través de los macrófagos y son eliminadas después mediante el drenaje linfático. Tras cada sesión, el pigmento es menos visible. 
Una vez finalizada la sesión, se aplica una crema antibiótica en la zona tratada y se cubre con una cura oclusiva. Esta cura debe realizarse a diario, hasta que la piel se recupere. Es normal que la zona tratada muestre una ligera inflamación, púrpuras y, en ocasiones, ampollas pequeñas que se producen por la explosión del pigmento bajo la piel.
El número total de sesiones varía en función del color y profundidad del tatuaje y del tipo de pigmento aplicado. Por lo general se requiere un mínimo de 6 sesiones, pero si el tatuaje es profesional y policromado, requerirá un número de sesiones mayor.
