            <p align="justify">La liposucción es una técnica quirúrgica utilizada para eliminar depósitos de grasa en determinadas áreas del cuerpo, incluyendo cara y cuello, brazos, tronco, abdomen, glúteos, caderas y muslos, rodillas, pantorrillas y tobillos. La liposucción no es un sustituto de la reducción de peso, sino un método para eliminar depósitos localizados de tejido graso que no responden a la dieta o el ejercicio.
La liposucción puede ser realizada como un procedimiento primario para mejorar el contorno corporal, o en combinación con otras técnicas quirúrgicas, como lifting facial, abdominoplastía, o lifting de muslos, para tensar la piel relajada y estructuras de soporte. El término “lipoescultura” se refiere a la combinación de liposucción y lipoinyección, es decir, la extracción de grasa de sitios específicos del cuerpo y su posterior inyección en sitios en los que se requiere un aumento de volumen, logrando así una mejoría importante en el contorno facial o corporal.
Se puede llevar a cabo de forma ambulatoria o con hospitalización postoperatoria de una noche en promedio y se realiza bajo anestesia general o mediante bloqueos regionales y sedación dependiendo de la región a tratar y 
la preferencia del paciente.
 El tiempo aproximado de recuperación para regresar a actividades cotidianas es variable dependiendo de la extensión de las áreas a tratar, y va de 3 a 10 días aproximadamente. Dentro de las restricciones durante el período de recuperación se encuentran: no asolearse durante al menos un mes, no cargar objetos pesados, no agacharse.
 Se debe utilizar una prenda compresiva especial (faja, barbicuello, manga, etc.) dependiendo del área tratada, para asegurar un resultado satisfactorio. La paciente puede realizar ejercicio ligero aproximadamente a las 3 semanas, y ejercicio intenso entre 5 y 7 semanas después de la cirugía. 
